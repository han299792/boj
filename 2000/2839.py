def asd_수정(n):
    # n이 5로 나누어 떨어지는 경우 (최대 5kg 봉지 사용)
    if n % 5 == 0:
        return n // 5
    
    a = n // 5  # 5kg 봉지의 최대 개수
    cnt_5 = a 
    m_남은 = a * 5 # 5kg 봉지로 채운 무게
    
    # 5kg 봉지 개수를 하나씩 줄여가며 남은 무게(n - m)가 3으로 나누어 떨어지는지 확인
    while cnt_5 >= 0: # cnt_5가 0이 될 때까지 (5kg 봉지 0개)
        남은_무게 = n - m_남은 
        
        if 남은_무게 >= 0 and 남은_무게 % 3 == 0:
            cnt_3 = 남은_무게 // 3 # 3kg 봉지 개수
            return cnt_5 + cnt_3
        
        cnt_5 -= 1 # 5kg 봉지 하나 줄임
        m_남은 -= 5  # 채운 무게 5 줄임

    # 위의 어떤 경우에도 해당하지 않으면 정확히 맞출 수 없음
    return -1